export function tokenize(text: string): string[] {
    return text
        .toLowerCase()
        .replace(/[^a-z0-9\s'-]/g, " ")
        .split(/\s+/)
        .filter((t) => t.length > 0 && !STOPWORDS.has(t));
}

export function isStopword(word: string): boolean {
    return STOPWORDS.has(word.toLowerCase());
}
